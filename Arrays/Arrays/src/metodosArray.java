public class metodosArray {

    public int n;

    public double m;

    public String palabra; 

    public Object objeto;

    //constructores 

    public metodosArray(int n, double m, String palabra, Object objeto) {
        this.n = n;
        this.m = m;
        this.palabra = palabra;
        this.objeto = objeto;
    }

    public metodosArray(int n) {
        this.n = n;
    }

    public metodosArray(double m) {
        this.m = m;
    }

    public metodosArray(String palabra) {
        this.palabra = palabra;
    }

    public metodosArray(int n, String palabra) {
        this.n = n;
        this.palabra = palabra;
    }

    // metodos 

}
